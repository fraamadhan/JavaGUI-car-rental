    public int checkRentTransaction() {
        Mobil mobilModel = new Mobil();
        Koneksi con = new Koneksi();
        ResultSet rs = null;

        try {
            rs = con.getData("SELECT stok FROM mobil WHERE mobil_id = '" + (String) jenisMobilComboBox.getSelectedItem() + "'");
            while (rs.next()) {
                int stok = rs.getInt("stok");
                mobilModel.setMobilId((String) jenisMobilComboBox.getSelectedItem());
                mobilModel.setStok(stok);
                if (mobilModel.mobilKeluar(stok) == 0) {
                    JOptionPane.showMessageDialog(null, "Mobil tidak tersedia" + " silakan pilih mobil jenis lain");
                    return 0;
                } else {
                    return 1;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return 1;
    }

